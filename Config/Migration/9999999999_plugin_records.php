App::uses('NetCommonsMigration', 'NetCommons.Config/Migration');

/**
 * Add plugin migration
 *
 * @package NetCommons\PluginManager\Config\Migration
 */
class PluginRecords extends NetCommonsMigration {

/**
 * Migration description
 *
 * @var string
 */
	public $description = 'plugin_records';

/**
 * Actions to be performed
 *
 * @var array $migration
 */
	public $migration = array(
		'up' => array(),
		'down' => array(),
	);

/**
 * plugin data
 *
 * @var array $migration
 */
	public $records = array(
		'Plugin' => array(
			'key' => 'plugin_manager',
			'namespace' => 'netcommons/plugin-manager',
			'name' => 'Plugin Manager',
			'type' => 2,
			'default_action' => 'plugin_manager/index',
			'default_setting_action' => '',
			'weight' => 5,
		),
		'PluginsRole' => array(
			array(
				'role_key' => 'system_administrator',
			),
		),
	);

/**
 * Before migration callback
 *
 * @param string $direction Direction of migration process (up or down)
 * @return bool Should process continue
 */
	public function before($direction) {
		return true;
	}

/**
 * After migration callback
 *
 * @param string $direction Direction of migration process (up or down)
 * @return bool Should process continue
 */
	public function after($direction) {
		$this->loadModels([
			'Plugin' => 'PluginManager.Plugin',
		]);

		if ($direction === 'down') {
			$this->Plugin->deletePlugin($this->records);
		} else {
			$this->Plugin->savePlugin($this->records);
		}
		return true;
	}
}
